pub fn getln(trim: bool) -> String{
    let mut input = String::new();
    match std::io::stdin().read_line(&mut input){
        Ok(_) => {
            if trim {
                input = String::from(input.trim());
            }
            return input;
        },
        Err(e) => { return e.to_string(); }
    }
}

